package com.task1;
import java.util.*;

public class ConditionalOperator 
{
	public static void main(String args[])
	{
		Scanner sc=new Scanner(System.in);
		System.out.println("enter a value ");
		int a=sc.nextInt();
		a=a++;
		System.out.println(a);
		a=--a;
		System.out.println(a);
		a=++a;
		System.out.println(a);
		a=a--;
		System.out.println(a);
				
	}
}
